package Tank;

public class Tank {
    private int x;
    private int y;

    public Tank(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }
    //
    public void setX(int x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }
    ///
    public void setY(int y) {
        this.y = y;
    }
}
